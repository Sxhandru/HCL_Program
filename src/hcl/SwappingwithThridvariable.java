package hcl;

public class SwappingwithThridvariable {
	public static void main(String[] args) {
		int a=3,b=2,c;
		System.out.println("Before swapping");
		System.out.println(a);
		System.out.println(b);
		c=a;
		a=b;
		b=c;
		System.out.println("After Swapping");
		System.out.println(a);
		System.out.println(b);
	}

}
