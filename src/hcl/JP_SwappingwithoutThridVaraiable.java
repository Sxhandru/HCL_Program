public class JP_SwappingwithoutThridVaraiable {
	public static void main(String[] args) {
	int a=10,b=5;
	System.out.println("Before Swapping value");
	System.out.println(a);
	System.out.println(b);
	a=a+b;
	b=a-b;
	a=a-b;
	System.out.println("After Swapping Value");
	System.out.println(a);
	System.out.println(b);
	
	
	
}
}
