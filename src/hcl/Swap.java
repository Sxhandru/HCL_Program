package hcl;

public class Swap {
	public static void main(String[] args) {
		int a=12,b=24,c;
		System.out.println("Original Value");
		System.out.println(a);
		System.out.println(b);
		c=a;
		a=b;
		b=c;
		System.out.println("Swap Value");
		System.out.println(a);
		System.out.println(b);
		
	}

}
